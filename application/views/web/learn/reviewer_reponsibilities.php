                            <h4 class="entry-title">Reviewer Duties and Reponsibilities</h4>
                            <p>Below you will find useful tools and resources to support you in your role as a Reviewer.</p>
                            <hr>
                        <div class="entry-title">
                            <a href="#">What is peer review?</a>
                        </div>
                        <p class="description">Peer review, also known as refereeing, is a collaborative process that allows manuscripts submitted to a journal to be evaluated and commented upon by independent experts within the same field of research. The evaluation and critique generated from peer review provides authors with feedback to improve their work and, critically, allows the editor to assess the paper’s suitability for publication in the journal. The peer-review process does receive much criticism and is not without its limitations; however, it remains a widely recognized standard in terms of journal quality.</p>
                    </div>
                    <hr>
                    <div class="top-info">
                    <div class="entry-title">
                            <a href="#">Why review?</a>
                        </div>
            <li><span class="text-theme">●</span> To maintain awareness of the current research emerging within your subject area.</li>
            <li><span class="text-theme">●</span> To help authors improve their papers, applying your professional expertise to help others. </li>
            <li><span class="text-theme">●</span> To assist in maintaining a good, rigorous peer-review process resulting in the publication of the best and brightest – you can have a part in championing &nbsp;&nbsp;&nbsp;&nbsp;the next key paper in your own field of interest.</li>
            <li><span class="text-theme">●</span> To build relationships with the editorial team of a journal and improve your academic and professional profile.</li>
            <li><span class="text-theme">●</span> Although often anonymous, the review process can act as a conversation between author, reviewer, and editor as to how the paper can be improved to &nbsp;&nbsp;&nbsp;&nbsp;maximize its impact and further research in the field. </li>
            <li><span class="text-theme">●</span> To maintain awareness of the current research emerging within your subject area and gain a sense of prestige in being consulted as an expert. </li>
                    </div>
                    <hr>
                            <div class="top-info">
                    <div class="entry-title">
                            <a href="#">What to consider before considering yourself to reviewing?</a>
                        </div>
                        <p>Before agreeing to review for a journal, you should take note of the following:</p>
            <li><span class="text-theme">●</span> What form of review does the journal operate? (single/blind/open)</li>
            <li><span class="text-theme">●</span> How you will need to submit your review – for example, is there a structured form for reviewers to complete or will you be required to write free text? </li>
            <li><span class="text-theme">●</span> Papers and correspondence sent to reviewers in the course of conducting peer review are to be dealt with as privileged confidential documents. </li>
            <li><span class="text-theme">●</span> If a conflict of interest exists, you should make the editor aware of this as soon as possible. </li>
            <li><span class="text-theme">●</span> Whether you are able to complete the level of review required by the editor in the allotted time – extensions can be provided or a brief report may suffice &nbsp;&nbsp;&nbsp;&nbsp;on some occasions. If you are struggling to meet the deadline, let the editor know, so they can inform the author if there is a delay.  </li>
                    </div>
                    <hr>
                     <div class="top-info">
                    <div class="entry-title">
                            <a href="#">How to conduct a review</a>
                        </div>
                        <p>Before you accept or decline an invitation to review, consider the following questions:</p>
            <li><span class="text-theme">●</span> Does the article match your area of expertise? Only accept if you feel you can provide a high quality review.</li>
            <li><span class="text-theme">●</span> Do you have a potential conflict of interest? Disclose this to the editor when you respond.</li>
            <li><span class="text-theme">●</span> Do you have time? Reviewing can be a lot of work – before you commit, make sure you can meet the deadline.</li>
            <li><span class="text-theme">●</span> Finally: Educate yourself on the peer review process through the journal. </li>
            <p>Respond to the invitation as soon as you can – delay in your decision slows down the review process, whether you agree to review or not. If you decline the invitation, provide suggestions for alternative reviewers.</p>

                    </div>
                    <hr>
                    <div class="top-info">
                    <div class="entry-title">
                            <a href="#">Before you start</a>
                        </div>
            <p>If you accept, you must treat the materials you receive as confidential documents. This means you can’t share them with anyone without prior authorization from the editor. Since peer review is confidential, you also must not share information about the review with anyone without permission from the editors and authors.<br>

First read the article and then take a break from it, giving you time to think. Consider the article from your own perspective. When you sit down to write the review, make sure you know what the journal is looking for, and have a copy of any specific reviewing criteria you need to consider.</p>

                    </div>
                    <hr>
                    <div class="top-info">
                    <div class="entry-title">
                            <a href="#">Your review report</a>
                        </div>
            <p>our review will help the editor decide whether or not to publish the article. Giving your overall opinion and general observations of the article is essential. Your comments should be courteous and constructive, and should not include any personal remarks or personal details including your name.<br>
Providing insight into any deficiencies is important. You should explain and support your judgment so that both editors and authors are able to fully understand the reasoning behind your comments. You should indicate whether your comments are your own opinion or are reflected by the data. </p>

                    </div>
                    <hr>
                    <div class="top-info">
                    <div class="entry-title">
                            <a href="#">Have the questions on your mind while reviewing</a>
                        </div>
            <li><span class="text-theme">●</span> Is the submission original? </li>
            <li><span class="text-theme">●</span> Is the research cutting edge or topical? </li>
            <li><span class="text-theme">●</span> Does it help to expand or further research in this subject area? </li>
            <li><span class="text-theme">●</span> Does it significantly build on (the author’s) previous work? </li>
            <li><span class="text-theme">●</span> 	Does the paper fit the scope of the journal? </li>
            <li><span class="text-theme">●</span> Would you recommend that the author reconsider the paper for a related or alternative journal? </li>
            <li><span class="text-theme">●</span> Should it be shortened and reconsidered in another form? </li>
            <li><span class="text-theme">●</span> Would the paper be of interest to the readership of the journal?  </li>
            <li><span class="text-theme">●</span> Is there an abstract or brief summary of the work undertaken as well as a concluding section?  </li>
            <li><span class="text-theme">●</span> Is the paper complete?</li>
            <li><span class="text-theme">●</span> Is the submission in Standard English to aid the understanding of the reader?</li>
              <li><span class="text-theme">●</span> For non-native speakers, an English editing service may be useful (see our Author Services website for advice). </li>
            <li><span class="text-theme">●</span> 	Is the methodology presented in the manuscript and any analysis provided both accurate and properly conducted? </li>
            <li><span class="text-theme">●</span> Do you feel that the significance and potential impact of a paper is high or low? Are all relevant accompanying data, citations, or references given by the &nbsp;&nbsp;&nbsp;&nbsp;author? </li>
