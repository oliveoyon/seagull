                            <a href="#"><span id="Publishing"></span>Publishing Ethic</a>
                        </div>
                        <p class="description">The publication of an article in a peer-reviewed journal is an essential building block in the development of a coherent and respected network of knowledge. It is a direct reflection of the quality of the work of the authors and the institutions that support them. Peer-reviewed articles support and embody the scientific method. It is therefore important to agree upon standards of expected ethical behavior for all parties involved in the act of publishing: the author, the journal editor, the peer reviewer, the publisher and the society of society-owned or sponsored journals.<br>
                        Seagull is prtnered with COPE (Committee on Publication Ethics), to learn more about publishing ethics of various person's role and responsibilities of an article publication to any of a journal please visit <a href="https://publicationethics.org"><span class="text-theme">COPE Website</span></a></p>
