                    <div class="top-info">
                    <div class="entry-title">
                            <a href="#"><span id="Reviewers"></span>Working with Reviewers</a>
                        </div>
                        <p class="description">Reviewers will help the editors to meet the deadlines of any project work. The Reviewers can help editors to work with reviewers, giving them the guidelines, information and support. Reviewers may take up their time with manuscripts to review that are of such poor quality they could be rejected articles on topics which are outside the reviewer's area of expertise. Reviewers generally like to know the final editorial decision of the paper, and see other reviewers’ comments.</p>
                    </div>
                    <hr>
                    <div class="top-info">
                    <div class="entry-title">
                            <a href="#"><span id="Support"></span>Supporting Authors</a>
                        </div>
                        <p class="description">Editors will find suitable collaborations for authors research. Editors will provide guidelines for the authors to enrich their professional research profile. Editors will help the authors for joining the cross discipline research to build potential collaborations about other research projects.</p>
                    </div>
                    <hr>
                    <div class="top-info">
                    <div class="entry-title">
                            <a href="#"><span id="Submission"></span>Submission Systems</a>
                        </div>
                        <p class="description">All submissions to our journal will be managed using one of our online submission systems. Using an online submission system, one can access and process submitted papers from anywhere with internet access. Different access levels are available for different editorial roles and there are various help and training resources at one’s disposal to support them with these systems.</p>
                    </div>
                    <hr>
                    <div class="top-info">
                    <div class="entry-title">
                            <a href="#"><span id="Boards"></span>Editorial Boards</a>
                        </div>
                        <p class="description">The editorial board, or (editorial) advisory board, is a team of experts in the journal's field. The members of editorial board will review submitted manuscripts. They will provide advice on journal policy and scope. They will try to identify topics for special issues. If they want, they may attract new authors and submissions and also act as advisers in the case of complex publishing ethics allegations.</p>
                    </div>
                    <hr>
                    <div class="top-info">
                    <div class="entry-title">
                            <a href="#"><span id="Guest"></span>Guest Editors</a>
                        </div>
                        <p class="description">Guest editors will play a vital role in acquiring content and leading the review process for special issue publications. Guest editors will be invited as an expert in an author’s specific field of research. One can submit a proposal for a special issue to a journal, the senior editor(guest) will approve or reject it after consulting with the editorial board.</p>
                    </div>
