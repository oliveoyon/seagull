							<h2 class="causes_single_title">Tax Policy after BEPS, what can be expected?</h2>

							<p class="cases_info_text">The 5TH Global Tax Policy Conference (GTPC) is set to
bring together renown experts, government officials,
CFOs and tax directors of multinationals enterprises, tax
advisors, non-governmental organizations and
academics that are interested in how states choose their
route and balance their interest in the new setting of
international tax rules.
Since the international hard and soft law legal
framework is plenty of changes and challenges for
business and governments the conference promises a
day of intensive and exciting debates. The Conference
deliberations will be on the following themes:</p>

							<p class="cases_info_text">1. BEPS 2.0<br>
- OECD-approach<br>
- EU. What is the future?<br>
2. GAAR, Abuse and International Taxation<br>
- Cases decided in the European Union<br>
- Cases decided by the German Courts and Revenue
Service<br>
- Latin American and regional approaches (round table
with representatives of Revenue Services, Academia,
NGO)<br>
3. Cooperation between Revenue Services<br>
4. Mandatory disclosure<br>
5. Dispute resolution</p>

